public class Q18 {
    public static void main(String[] args) {
        // Box 1 dimensions
        int width1 = 110;
        int height1 = 20;
        int depth1 = 15;

        // Box 2 dimensions
        int width2 = 23;
        int height2 = 6;
        int depth2 = 9;

        // Calculate volumes
        int volume1 = width1 * height1 * depth1;
        int volume2 = width2 * height2 * depth2;

        // Display results
        System.out.println("Volume of Box 1 = " + volume1);
        System.out.println("Volume of Box 2 = " + volume2);
    }
}
